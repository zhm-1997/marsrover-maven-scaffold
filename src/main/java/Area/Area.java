package Area;

public class Area {
    private int x;
    private int y;


    public Area(int x, int y) {

        this.x = x;
        this.y = y;
    }

    public int containsPointWidth(int x) {
        return this.x;
    }

    public int containsPointHeight(int y) {
        return this.y;
    }
}
